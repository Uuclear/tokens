//! Generate theme-styled platform logos from `src/serve/assets/logos/*`.
//!
//! Run: `cargo run --bin generate_theme_logos`

use image::imageops::FilterType;
use image::{DynamicImage, Rgba};
use std::fs;
use std::path::{Path, PathBuf};

const PLATFORMS: &[&str] = &[
    "claude_code",
    "codex",
    "cursor",
    "opencode",
    "openclaw",
    "hermes",
    "qwen_code",
    "pi",
    "cline",
    "kilo_cli",
    "kilo_ide",
    "cherry_studio",
    "chatbox",
    "qoder",
    "qoder_cn",
    "postman",
    "dify",
];

const THEMES: &[&str] = &["pixel", "terminal", "ink", "paper"];

fn main() -> anyhow::Result<()> {
    let root = PathBuf::from(env!("CARGO_MANIFEST_DIR"));
    let logos_dir = root.join("src/serve/assets/logos");
    let out_root = logos_dir.join("themes");
    let rs_out = root.join("src/serve/themed_logos.rs");

    fs::create_dir_all(&out_root)?;
    let mut arms = Vec::new();

    for theme in THEMES {
        fs::create_dir_all(out_root.join(theme))?;
    }

    for id in PLATFORMS {
        let Some(src) = load_source(&logos_dir, id) else {
            eprintln!("skip {id}: no source png/ico");
            continue;
        };
        for theme in THEMES {
            let styled = match *theme {
                "pixel" => pixel_theme(&src),
                "terminal" => terminal_theme(&src),
                "ink" => ink_theme(&src),
                "paper" => paper_theme(&src),
                _ => continue,
            };
            let rel = format!("assets/logos/themes/{theme}/{id}.png");
            let out_path = logos_dir.join("themes").join(theme).join(format!("{id}.png"));
            styled.save(&out_path)?;
            arms.push(format!(
                "        (\"{theme}\", \"{id}\") => Some((\"image/png\", include_bytes!(\"{rel}\"))),"
            ));
        }
        println!("OK {id}");
    }

    let body = if arms.is_empty() {
        r#"// No themed logos generated — run fetch-logos then this tool again.

pub fn themed_logo_bytes(_theme: &str, _platform_id: &str) -> Option<(&'static str, &'static [u8])> {
    None
}
"#
        .to_string()
    } else {
        format!(
            "// Auto-generated by `cargo run --bin generate_theme_logos`. Do not edit.\n\n\
             pub fn themed_logo_bytes(theme: &str, platform_id: &str) -> Option<(&'static str, &'static [u8])> {{\n\
             match (theme, platform_id) {{\n{arms}\n\
             _ => None,\n\
             }}\n\
             }}\n",
            arms = arms.join("\n")
        )
    };

    fs::write(&rs_out, body)?;
    println!("Wrote {}", rs_out.display());
    Ok(())
}

fn load_source(dir: &Path, id: &str) -> Option<DynamicImage> {
    let candidates: Vec<String> = match id {
        "qoder_cn" => vec!["qoder_cn".into(), "qoder".into()],
        "kilo_ide" => vec!["kilo_ide".into(), "kilo_cli".into()],
        _ => vec![id.into()],
    };
    for base in &candidates {
        for ext in ["png", "ico"] {
            let p = dir.join(format!("{base}.{ext}"));
            if p.exists() {
                if let Ok(img) = image::open(&p) {
                    return Some(img);
                }
            }
        }
    }
    None
}

fn luminance(p: &Rgba<u8>) -> u8 {
    ((0.299 * f32::from(p[0])) + (0.587 * f32::from(p[1])) + (0.114 * f32::from(p[2]))) as u8
}

fn resize_square(img: &DynamicImage, px: u32) -> DynamicImage {
    img.resize_exact(px, px, FilterType::Triangle)
}

fn pixel_theme(img: &DynamicImage) -> DynamicImage {
    let small = img.resize_exact(16, 16, FilterType::Nearest);
    let mut buf = small.to_rgba8();
    for p in buf.pixels_mut() {
        if p[3] < 40 {
            *p = Rgba([0, 0, 0, 0]);
            continue;
        }
        let l = luminance(p);
        let v = if l > 210 {
            255
        } else if l > 120 {
            160
        } else if l > 60 {
            80
        } else {
            0
        };
        *p = Rgba([v, v, v, 255]);
    }
    DynamicImage::ImageRgba8(buf).resize_exact(48, 48, FilterType::Nearest)
}

fn terminal_theme(img: &DynamicImage) -> DynamicImage {
    let mut buf = resize_square(img, 48).to_rgba8();
    for p in buf.pixels_mut() {
        if p[3] < 40 {
            *p = Rgba([0, 0, 0, 0]);
            continue;
        }
        let l = luminance(p);
        if l < 28 {
            *p = Rgba([0, 0, 0, 0]);
            continue;
        }
        let g = ((f32::from(l) - 20.0) * 1.15).min(255.0) as u8;
        *p = Rgba([0, g.min(220), 0, 255]);
    }
    DynamicImage::ImageRgba8(buf)
}

fn ink_theme(img: &DynamicImage) -> DynamicImage {
    let mut buf = resize_square(img, 48).to_rgba8();
    for p in buf.pixels_mut() {
        if p[3] < 40 {
            *p = Rgba([0, 0, 0, 0]);
            continue;
        }
        let l = luminance(p);
        let v = if l > 200 {
            0
        } else if l > 140 {
            60
        } else if l > 80 {
            120
        } else {
            200
        };
        *p = Rgba([v, v, v, 255]);
    }
    DynamicImage::ImageRgba8(buf)
}

fn paper_theme(img: &DynamicImage) -> DynamicImage {
    let mut buf = resize_square(img, 48).to_rgba8();
    for p in buf.pixels_mut() {
        if p[3] < 40 {
            *p = Rgba([0, 0, 0, 0]);
            continue;
        }
        let l = luminance(p);
        let t = l as f32 / 255.0;
        let r = (220.0 + t * 35.0).min(255.0) as u8;
        let g = (190.0 + t * 50.0).min(255.0) as u8;
        let b = (150.0 + t * 40.0).min(255.0) as u8;
        let gray = (r as u16 + g as u16 + b as u16) / 3;
        *p = Rgba([gray as u8, (gray as u16 * 95 / 100) as u8, (gray as u16 * 80 / 100) as u8, 255]);
    }
    DynamicImage::ImageRgba8(buf)
}
